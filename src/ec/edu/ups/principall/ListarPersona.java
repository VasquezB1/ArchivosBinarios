    public void llenarDatos(){
        modelo = (DefaultTableModel) tabladatos.getModel();
        int controladorLista = 0;
        int contador = 0;
        int posicion;
         while (contador == 0) {
            try {
                String ruta = "archivo.ups";
               RandomAccessFile archivo = new RandomAccessFile(ruta, "r");
                posicion = controladorLista * 152;
                archivo.seek(posicion);
               
                    String nombre = archivo.readUTF().trim();
                    archivo.seek(posicion + 52);
                    String apellido = archivo.readUTF().trim();
                    archivo.seek(posicion + 104);
                    String cedula = archivo.readUTF();
                    archivo.seek(posicion + 116);
                    int edad = archivo.readInt();
                    archivo.seek(posicion + 120);
                    String fecha = archivo.readUTF();
                    archivo.seek(posicion + 132);
                    String celular = archivo.readUTF();
                    archivo.seek(posicion + 144);
                    double salario = archivo.readDouble();
                    Object[] datos = {
                        cedula,
                        apellido,
                        nombre,
                        edad,
                        celular,                        
                        salario,
                        fecha,
                    controladorLista};
                    modelo.addRow(datos);
                
                controladorLista++;
            }catch (FileNotFoundException e8) {
             JOptionPane.showMessageDialog(this, "Su error es: \n" + e8.toString(), "", JOptionPane.WARNING_MESSAGE);
        } catch (IOException e9) {
            contador=1;
            //JOptionPane.showMessageDialog(this, "Su error es: \n" + e9.toString(), "", JOptionPane.WARNING_MESSAGE);
        } 
         }
    }
    
